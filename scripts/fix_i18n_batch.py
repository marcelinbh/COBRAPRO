"""
Script para corrigir textos hardcoded em múltiplas páginas do CobraPro.
Foco nas páginas com mais textos hardcoded e sem t().
"""
import json
import re
import os

BASE = '/home/ubuntu/cobrapro/client/src'
LOCALES = '/home/ubuntu/cobrapro/client/src/i18n/locales'

def load_json(fname):
    with open(f'{LOCALES}/{fname}.json', encoding='utf-8') as f:
        return json.load(f)

def save_json(fname, data):
    with open(f'{LOCALES}/{fname}.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def add_keys(section, ptbr_keys, es_keys):
    """Add keys to both locale files"""
    for fname, keys in [('pt-BR', ptbr_keys), ('es', es_keys)]:
        data = load_json(fname)
        if section not in data:
            data[section] = {}
        data[section].update(keys)
        save_json(fname, data)
    print(f'✅ Added {len(ptbr_keys)} keys to section "{section}"')

def fix_file(filepath, replacements):
    """Apply text replacements to a file"""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    count = 0
    for old, new in replacements:
        if old in content:
            content = content.replace(old, new)
            count += 1
        # else:
        #     print(f'  ❌ Not found: {old[:50]}')
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f'✅ Fixed {count}/{len(replacements)} in {os.path.basename(filepath)}')

